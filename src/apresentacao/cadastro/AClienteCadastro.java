    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        try {
            int resposta = JOptionPane.showConfirmDialog(null,"Confirmar a exclus√£o do cliente",
                                                         "Sistema Comercial", JOptionPane.YES_OPTION);
            if(resposta == JOptionPane.YES_OPTION){
                NCliente nCliente = new NCliente();
                nCliente.excluir(Integer.parseInt(jTextFieldCodigo.getText()));
                JOptionPane.showMessageDialog(null, "Opera√ß√£o realizada com sucesso!");
                LimparTela();
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

